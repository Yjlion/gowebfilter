  const STORAGE_KEY = "wf_lang";

  function langMeta(code) {
    return LANGS.find((l) => l.code === code) || LANGS[0];
  }

  function getLang() {
    const stored = (function () {
      try { return localStorage.getItem(STORAGE_KEY); } catch (e) { return null; }
    })();
    return I18N[stored] ? stored : "en";
  }

  function wfT(lang, key) {
    const table = I18N[lang] || I18N.en;
    if (key in table) return table[key];
    if (key in I18N.en) return I18N.en[key];
    return undefined;
  }

  // Plain (non-reactive) translator used by applyI18n and outside Alpine.
  function t(key, fallback) {
    const v = wfT(getLang(), key);
    return v === undefined ? (fallback !== undefined ? fallback : key) : v;
  }

  function applyDir(lang) {
    const meta = langMeta(lang);
    document.documentElement.lang = lang;
    document.documentElement.dir = meta.dir;
  }

  function applyI18n(root) {
    root = root || document;
    root.querySelectorAll("[data-i18n]").forEach((el) => {
      const v = wfT(getLang(), el.getAttribute("data-i18n"));
      if (v !== undefined) el.textContent = v;
    });
    root.querySelectorAll("[data-i18n-html]").forEach((el) => {
      const v = wfT(getLang(), el.getAttribute("data-i18n-html"));
      if (v !== undefined) el.innerHTML = v;
    });
    root.querySelectorAll("[data-i18n-ph]").forEach((el) => {
      const v = wfT(getLang(), el.getAttribute("data-i18n-ph"));
      if (v !== undefined) el.setAttribute("placeholder", v);
    });
    root.querySelectorAll("[data-i18n-title]").forEach((el) => {
      const v = wfT(getLang(), el.getAttribute("data-i18n-title"));
      if (v !== undefined) el.setAttribute("title", v);
    });
  }

  // Inject a small version label in the sidebar's bottom corner. The sidebar is
  // duplicated per page but always an <aside>; the login page has none (guarded).
  // The version is reported by the server (/api/version), which reflects the
  // GitHub release tag.
  function injectVersion() {
    const aside = document.querySelector("aside");
    if (!aside || aside.querySelector(".wf-version")) return;
    const el = document.createElement("div");
    el.className = "wf-version px-6 pb-3 text-[10px] text-gray-600 select-none";
    aside.appendChild(el);
    fetch("/api/version")
      .then((r) => (r.ok ? r.json() : null))
      .then((d) => { if (d && d.version) el.textContent = d.version; })
      .catch(() => {});
  }

